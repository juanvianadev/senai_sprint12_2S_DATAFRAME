import pandas as pd
import plotly.express as px

vendas = pd.read_excel('Vendas.xlsx')
# print(vendas)

vendas.head()

print(vendas.dtypes)

vendasLoja = vendas.groupby('ID Loja')['Valor Final'].sum()
vendasLoja = vendasLoja.sort_values(ascending=False)
print(vendasLoja)

vendasQuantidade = vendas.groupby('ID Loja')['Quantidade'].sum()
vendasQuantidade = vendasQuantidade.sort_values(ascending=False)
print(vendasQuantidade)

maisVendida = vendas.sort_values(by='Quantidade', ascending=False)
maisVendida.head(10)

vendasFiltro = vendas.groupby('Produto')['Quantidade'].sum()
maisVendido = vendasFiltro.sort_values(ascending=False).head()
print(maisVendido)